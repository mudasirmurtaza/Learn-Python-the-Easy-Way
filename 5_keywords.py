""" keywords are reserved words in python
Keywords can't be name of identifier/variable
Keywords in Python** are reserved words that have specific meanings and cannot be used as variable names or identifiers. They are essential for the structure and functionality of Python programs.

Here is a list of commonly used keywords in Python:

Control Flow Keywords:

* if: Executes a block of code if a condition is true.
* else: Executes a block of code if a condition is false.
* elif: Executes a block of code if a previous condition was false and this one is true.
* for: Iterates over a sequence (e.g., list, tuple, string).
* while: Repeats a block of code as long as a condition is true.
* break: Exits a loop.
* continue: Skips the current iteration of a loop and continues with the next one.

Data Definition Keywords:

* and: Logical AND operator.
* or: Logical OR operator.
* not: Logical NOT operator.
* in: Checks if an element is present in a sequence.
* is: Checks if two objects are the same.
* None: Represents the absence of a value.
* True: Represents the boolean value "true".
* False: Represents the boolean value "false".
* del: Deletes an object.
* assert: Checks if a condition is true, raises an AssertionError if false.

Function and Class Definition Keywords:

* def: Defines a function.
* class: Defines a class.
* return: Returns a value from a function.
* yield: Returns a generator object.

Import Keywords:

* import: Imports a module.
* from: Imports specific objects from a module.

Other Keywords:

* try: Starts a try block for exception handling.
* except: Handles exceptions raised in a try block.
* raise: Raises an exception.
* finally: Executes a block of code regardless of whether an exception is raised.
* with: Executes a block of code with context management.
* global: Declares a variable as global.
* nonlocal: Declares a variable as nonlocal.
* pass: A statement that does nothing.

It's important to avoid using keywords as variable names or identifiers to ensure your Python code runs correctly and without errors. 

Python is a case sensitive language """
